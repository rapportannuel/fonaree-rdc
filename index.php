              <h4 class="title"><a href="">Préface</a></h4>
            <blockquote>
              <p class="description">Le Fonds National REDD+ (FONAREDD) a été établi en 2012 par le Gouvernement de la République Démocratique du Congo (RDC) pour appuyer l’opérationnalisation de sa Stratégie nationale REDD à travers la mobilisation des financements, la coordination intersectorielle et le suivi de la mise en œuvre des programmes liés à la REDD+. 
                  
                  Il a pour objectif ultime de contribuer au maintien et à la gestion durable du deuxième plus grand bassin forestier du monde tout en assurant l’amélioration des niveaux de vie et des revenus de la population congolaise.
                  
                  Ce double-objectif a été ancré dans la Lettre d’intention signée entre la RDC et l’Initiative pour la forêt de l’Afrique centrale (CAFI) en 2016, à travers laquelle CAFI s’est engagé à financer une partie du Plan d’investissement REDD+ de la RDC, en échange de l’engagement de cette dernière à l’atteinte d’un ensemble de jalons sectoriels définis de manière conjointe par les deux partenaires.&nbsp;</p>
